<grostitre>Introduction</grostitre>

<h3>Le Québec en cartes. Littéralement.</h3>
<p><em>Magic the Listerine</em> est un projet collaboratif qui transforme la culture québécoise — dans tout ce qu’elle a de beau, de bizarre et de braillard — en cartes inspirées de l’univers de <em>Magic: The Gathering</em>.</p>
<p>Personnalités publiques, moments viraux, mèmes oubliés, débats de société, légendes urbaines et références de sous-sol de taverne : tout est matière à création.</p>

<p>Chaque carte est illustrée avec soin dans le style des véritables cartes Magic, mais avec une saveur 100 % locale — tantôt satirique, tantôt nostalgique, toujours ancrée dans notre réalité.</p>

<p>Ce projet est :</p>

<ul>
    <li><strong>Communautaire</strong> : chacun peut proposer des idées, des mécaniques ou des illustrations.</li>
    <li><strong>Open source</strong> : tout le monde peut contribuer, améliorer, ou s’en inspirer.</li>
    <li><strong>Créatif et irrévérencieux</strong> : parce que le fun passe avant le sérieux.</li>
</ul>

<p>Que vous soyez fan de Magic, accro à la politique québécoise, ou juste curieux de voir une carte "Rage Quit" illustrée par Monique, bienvenue dans <em>Magic the Listerine</em>.</p>


